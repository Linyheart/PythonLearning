# 6.演示系列(序列，即list等)解包(2.4.5)
print("要了解解包，首先就要了解打包。\n把多个值赋给一个变量时，Python会自动的把多个值封装成元组，称为序列打包。")
print("我们运行代码 a = 1, 2, 3 ，则他们就被自动打包进了 a 这个元组里：")
a = 1, 2, 3
print("a = "+str(a))
print("当我们把一个系列对象赋值给多个对象时，Python会自动帮我们解包，对应赋值。")
print("我们运行代码 c, d, e = a ，则 a 这个元祖被解包并分别赋值给了 c, d, e：")
c, d, e = a
print("c = "+str(c))
print("d = "+str(d))
print("e = "+str(e))
